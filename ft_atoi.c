int	ft_atoi(char *str)
{
	int		d;
	int		f;

	d = 0;
	f = 1;
	while ((*str <= '\r' && *str >= '\t') || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			f = -1;
		str++;
	}
	while (*str != '\0')
	{
		if (*str > '9' || *str < '0')
			return (f * d);
		d = d * 10 + *str - '0';
		str++;
	}
	return (f * d);
}
